export default function HelpScreen({ navigate }) {
  const { colors: C, gs } = useTheme();
  const insets = useSafeAreaInsets();
  const styles = useMemo(() => createStyles(C, insets), [C, insets]);
  const [openSection, setOpenSection] = useState(null);
  const [openItem, setOpenItem] = useState(null);

  const toggleSection = (id) => {
    setOpenSection(openSection === id ? null : id);
    setOpenItem(null);
  };

  const toggleItem = (key) => {
    setOpenItem(openItem === key ? null : key);
  };

  return (
    <View style={styles.container}>
      <View style={styles.header}>
        <TouchableOpacity onPress={() => navigate('lists')} style={styles.backBtn}>
          <Text style={styles.backBtnText}>‹ Назад</Text>
        </TouchableOpacity>
        <View>
          <Text style={styles.headerTitle}>Помощь</Text>
          <Text style={styles.headerSubtitle}>Как пользоваться приложением</Text>
        </View>
      </View>

      <ScrollView style={{ flex: 1 }} contentContainerStyle={styles.content}>
        <View style={[gs.card, styles.introCard]}>
          <Text style={styles.introText}>
            Это приложение помогает составлять списки покупок, следить за ценами и не забывать нужные товары.
            Нажмите на раздел чтобы раскрыть его.
          </Text>
        </View>

        {SECTIONS.map(section => (
          <View key={section.id} style={gs.card}>
            <TouchableOpacity
              style={styles.sectionHeader}
              onPress={() => toggleSection(section.id)}
            >
              <Text style={styles.sectionEmoji}>{section.emoji}</Text>
              <Text style={styles.sectionTitle}>{section.title}</Text>
              <Text style={styles.arrow}>{openSection === section.id ? '▲' : '▼'}</Text>
            </TouchableOpacity>

            {openSection === section.id && section.items.map((item, i) => {
              const key = `${section.id}_${i}`;
              return (
                <View key={key} style={styles.faqItem}>
                  <TouchableOpacity
                    style={styles.question}
                    onPress={() => toggleItem(key)}
                  >
                    <Text style={styles.questionText}>{item.q}</Text>
                    <Text style={styles.questionArrow}>{openItem === key ? '−' : '+'}</Text>
                  </TouchableOpacity>
                  {openItem === key && (
                    <Text style={styles.answer}>{item.a}</Text>
                  )}
                </View>
              );
            })}
          </View>
        ))}
      </ScrollView>
    </View>
  );
}

